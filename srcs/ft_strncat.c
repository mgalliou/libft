#include <libft.h>

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*s;

	s = s1 + ft_strlen(s1);
	while (n && *s2 && (*s++ = *s2++))
		--n;
	*s = '\0';
	return (s1);
}
